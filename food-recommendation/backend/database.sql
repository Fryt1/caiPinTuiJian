-- 用户表
CREATE TABLE users (
    id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT '用户ID',
    username VARCHAR(50) NOT NULL UNIQUE COMMENT '用户名',
    password VARCHAR(100) NOT NULL COMMENT '密码（加密）',
    email VARCHAR(100) NOT NULL UNIQUE COMMENT '邮箱',
    phone VARCHAR(20) COMMENT '手机号',
    role ENUM('USER', 'ADMIN') DEFAULT 'USER' COMMENT '角色',
    enabled BOOLEAN DEFAULT TRUE COMMENT '账户是否启用',
    create_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT '注册时间'
);

-- 用户健康信息表
CREATE TABLE user_health_info (
    id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT '健康信息ID',
    user_id BIGINT NOT NULL COMMENT '用户ID',
    height_cm DECIMAL(5,2) COMMENT '身高（cm）',
    weight_kg DECIMAL(5,2) COMMENT '体重（kg）',
    health_goal ENUM('减脂', '增肌', '维持') COMMENT '健康目标',
    taste_preference VARCHAR(50) COMMENT '口味偏好（如清淡、重口）',
    diet_preference VARCHAR(100) COMMENT '饮食偏好（如素食、低糖、低脂）',
    allergies TEXT COMMENT '过敏源（英文逗号分隔）',
    update_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE
);

-- 食物表
CREATE TABLE foods (
    id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT '食物ID',
    name VARCHAR(100) NOT NULL COMMENT '食物名称',
    category VARCHAR(50) COMMENT '食物类别（如低糖、低脂、素食）',
    calories DECIMAL(10,2) COMMENT '热量（Kcal）',
    protein DECIMAL(10,2) COMMENT '蛋白质（g）',
    fat DECIMAL(10,2) COMMENT '脂肪（g）',
    carbohydrate DECIMAL(10,2) COMMENT '碳水化合物（g）',
    ingredients TEXT COMMENT '成分列表（英文逗号分隔）',
    image_url VARCHAR(255) COMMENT '图片URL',
    description TEXT COMMENT '描述',
    create_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间'
);

-- 推荐记录（可用于历史追踪）
CREATE TABLE recommendation_history (
    id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT '记录ID',
    user_id BIGINT NOT NULL COMMENT '用户ID',
    recommended_food_ids TEXT COMMENT '推荐食物ID（英文逗号分隔）',
    filter_info TEXT COMMENT '过滤信息（过敏源）',
    create_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE
);


